package gov.df.fap.service.util.exceptions.gl;

/**
 * 正在执行月结的异常.
 * 如果入账时正在执行月结,则抛出此异常
 * @author lwq
 *
 */
public class ExecutingMonthlyBalanceException extends Exception {

  private static final long serialVersionUID = 1L;

  public ExecutingMonthlyBalanceException(String message) {
		super(message);
	}

	public ExecutingMonthlyBalanceException(String message, Throwable cause) {
		super(message, cause);
	}
}
